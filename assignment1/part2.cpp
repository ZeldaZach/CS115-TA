#include <iostream>

int main()
{
	int x = 10, y = 2;
	
	std::cout << x << " + " << y << " = " << x + y << std::endl;
	std::cout << x << " - " << y << " = " << x - y << std::endl;
	std::cout << x << " * " << y << " = " << x * y << std::endl;
	std::cout << x << " / " << y << " = " << x / y << std::endl;
	std::cout << x << " % " << y << " = " << x % y << std::endl;
}